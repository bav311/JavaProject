package javarush;

public class Two {
    private int x;
    private int y;

    public Two(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int mulply(int x, int y) {
        return x * y;
    }
}